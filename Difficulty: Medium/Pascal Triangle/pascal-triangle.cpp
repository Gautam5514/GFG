class Solution {
  public:
    vector<int> nthRowOfPascalTriangle(int n) {
        // code here
         vector<int> ans(n, 1);
       for(int i=1;i<n;i++)
       {
           for(int j=i-1;j>0;j--)
           {
               ans[j]=ans[j]+ans[j-1];
           }
       }
        return ans;
    }
};



//{ Driver Code Starts.

void printAns(vector<int> &ans) {
    for (auto &x : ans) {
        cout << x << " ";
    }
    cout << "\n";
}

int main() {
    int t;
    cin >> t;
    while (t--) {
        int n;
        cin >> n;
        Solution ob;
        auto ans = ob.nthRowOfPascalTriangle(n);
        printAns(ans);

        cout << "~" << endl;
    }
    return 0;
}

