class Solution {
  public:
    // Function to find the vertical width of a Binary Tree.
    void help(Node* root, int &less, int &more, int pos) {
        if(!root) return;
        
        less = min(less, pos); // finding the root to left store in -ve value
        more = max(more, pos); // finding the root too right store in +ve value
        help(root -> left, less, more, pos - 1); // traverse left side
        help(root -> right, less, more, pos + 1); // traverse right side
    }
    int verticalWidth(Node* root) {
        // code here
        int less = 0;
        int more = 0;
        if(!root) return 0;
        help(root, less, more, 0); // from the root to all tree from vertically
        return more + abs(less) + 1; // In ver. if less store in -ve value then make it (abs to positive) and add all -ve and -ve and + 1 for 0 mean root
    } 
};


